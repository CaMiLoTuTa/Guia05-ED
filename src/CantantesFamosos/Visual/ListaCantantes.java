    public static void mostrar() {

        if (ListaCantantesFamosos.cantantes.isEmpty()) {
            JOptionPane.showMessageDialog(null, "NO HAY CANTANTES REGISTRADOS.");

        } else {
            ListaCantantesFamosos.ordenar();

            lisCan.setVisible(true);

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Puesto");
            model.addColumn("Nombre");
            model.addColumn("Disco");
            model.addColumn("Ventas");

            int cont = ListaCantantesFamosos.cantantes.size() + 1;

            for (CantanteFamoso i : ListaCantantesFamosos.cantantes) {
                cont--;
                String nom = i.getNombre();
                String disc = i.getDiscoMasFamoso();
                int vent = i.getDiscoVentas();

                Object[] fila;

                fila = new Object[]{cont, nom, disc, vent};

                model.insertRow(0, fila);
            }

            tbListado.setModel(model);
            tbListado.setEnabled(false);

        }
    }

